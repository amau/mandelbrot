
package org.agutierrez.fractal.core;

public abstract class EscapeTimeInfo
{
	protected int n;

	/**
	 * Simple get method for the n attribute.
	 * 
	 * @return
	 */
	public int getN()
	{
		return n;
	}

/**
 * Simple set method for the n attribute
 * @param n
 */
	public void setN(int n)
	{
		this.n = n;
	}

}
